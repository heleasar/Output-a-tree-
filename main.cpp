#include <iostream>

int main() {
    // Output the tree
    std::cout << "   *" << std::endl;
    std::cout << "  ***" << std::endl;
    std::cout << " *****" << std::endl;
    std::cout << "*******" << std::endl;
    std::cout << "  ***" << std::endl;

    // Output two blank lines
    std::cout << std::endl << std::endl;

    // Output the cat
    std::cout << "/\\   /\\" << std::endl;
    std::cout << "  o o" << std::endl;
    std::cout << " =   =" << std::endl;
    std::cout << "  ---" << std::endl;

    return 0;
}
